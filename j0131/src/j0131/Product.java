package j0131;

public class Product {
	private String name;
	private int price;
	private int point;
	
	
	public String getName() {
		return name;
	}
	public int getPrice() {
		return price;
	}
	public int getPoint() {
		return point;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public void setPoint(int point) {
		this.point = point;
	}
}
